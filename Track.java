import javax.swing.ImageIcon;

public class Track{

  private String title;
  private String artist;
  private ImageIcon icon;
  
  public Track(){

  }

  public String getTitle(){
    return this.title;
  }

  public String getArtist(){
    return this.artist;
  }

  public ImageIcon getIcon(){
    return this.icon;
  }
}
